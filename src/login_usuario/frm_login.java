    private void btt_entrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btt_entrarActionPerformed

        ImageIcon img_gif;
        img_gif = new ImageIcon(getClass().getResource("/gifs/ezgif.com-resize.gif"));
        Icon icono_gif = new ImageIcon(img_gif.getImage().getScaledInstance(jLabel4.getWidth(), jLabel4.getHeight(), Image.SCALE_DEFAULT));
        jLabel4.setIcon(img_gif);

        String busquea_usuario = metodos.buscarUsuarioRegistrado(txt_correo.getText(), txt_contra.getText());

        if (txt_correo.getText().equals("root") && txt_contra.getText().equals("root")) {
            JOptionPane.showMessageDialog(this, "Bienvenido iniciaste secion como root (Administrador)", "Inicio de Admin", JOptionPane.ERROR_MESSAGE);
            frm_sistema ventana = new frm_sistema();
            ventana.lbl_nombre.setText(" Administrador");
            ventana.setVisible(true);
            this.dispose();

        } else if (busquea_usuario.equals("Usuario encontrado")) {
            String busqueda_nombre = metodos.buscarNombre(txt_correo.getText());
           
          
            final JOptionPane pane = new JOptionPane("Bienvenido \n" + busqueda_nombre);
            final JDialog d = pane.createDialog((JFrame) null, "Te damos la Bienvenida");
            d.setLocation(233, 200);
            d.setVisible(true);

            //JOptionPane.showMessageDialog(this, "Bienvenido \n" + busqueda_nombre, "Bienvenido", JOptionPane.ERROR_MESSAGE);
            frm_sistema ventana = new frm_sistema();
            ventana.lbl_nombre.setText(busqueda_nombre);
            ventana.setVisible(true);
            this.dispose();

        } else {

            JOptionPane.showMessageDialog(this, "Usuario no registrado, Cree un usuario nuevo", "Usuario no encontrado", JOptionPane.ERROR_MESSAGE);
            frm_nuevo_usuario ventana = new frm_nuevo_usuario();
            ventana.setVisible(true);
            this.setVisible(false);
        }

    }//GEN-LAST:event_btt_entrarActionPerformed

